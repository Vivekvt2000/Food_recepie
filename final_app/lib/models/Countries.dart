import 'dart:ffi';

class Countries {
  String name;
  String imgpth;
  String rating;
  String foodname;
  String des;


  Countries({
    required this.name,
    required this.imgpth,
    required this.rating,
    required this.foodname,
    required this.des
 });

  String get _name => name;

  String get _imagepath => imgpth;

  String get _rating => _rating;
  String get _foodname => _foodname;
  String get _des => _des;





}




